private void configureLabels() {
    Color originalColor = new Color(255, 255, 255, 10);
    
        
        // Configurar los JLabel para que se comporten como botones y resalten
        configureLabel(RegisterPeople, originalColor, new Color(255,255,255));
        configureLabel(ManageStaff, originalColor, new Color(255,255,255));
        configureLabel(ManageCourses, originalColor, new Color(255,255,255));
        configureLabel(ManageRegistrations, originalColor, new Color(255,255,255));


    }

    private void configureLabel(JLabel label, Color originalColor, Color clickColor) {
        label.setOpaque(true); 
        label.setBackground(originalColor); 

        label.addMouseListener(new MouseAdapter() {
            
          

         

            @Override
            public void mousePressed(MouseEvent e) {
                label.setBackground(clickColor);
            }

          
            
        });
    }
